public class Prueba{

int id;
String nombre;
int puntos;

public void getId(){
  
  return this.id;
}

public void getNombre(){
  
  return this.nombre;
}
public void getPuntos(){
  
  return this.puntos;
}

public Prueba(int id, String nombre, int puntos){
	
	this.id= id;
	this.nombre=nombre;
	this.puntos=puntos;
	
}

}
